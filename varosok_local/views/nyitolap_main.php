    <br>Városok<br>
    <br>
Hazánkban a városok száma 1990-től dinamikusan emelkedett, 2019-re megközelítette a 350-et. A várossá nyilvánítás szabályai 2015-ben jelentősen szigorodtak, így a közeljövőben kevés település kaphat városi rangot. Az adatbázis a jelenlegi városok és Budapest kerületeinek néhány adatát tárolja. A városok lélekszámát a várossá nyilvánítás évét követő évtől évente tartalmazza.
Olyan települések esetében, amelyek történetük során többször is várossá lettek, a várossá nyilvánítás éve az az év, amióta megszakítás nélkül viselik e címet. Azok az egykori városok, melyek korábban végleg elvesztették városi rangjukat, nem szerepelnek a felsorolásban.
Jelenleg legalább tízezer fős, az elmúlt öt évben folyamatosan növekvő népességhez, valamint intézményi és infrastrukturális paraméterekhez kötik a városi rang elnyerését. Ezzel összhangban 2013 és 2021 között egy település sem kapta meg a városi rangot, és 2021-ben is csak két új város lett.

